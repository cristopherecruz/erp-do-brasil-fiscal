type Pais struct {
	Codigo     int
	Descricao  string
	DataInicio time.Time
}

func NovoPais(codigo int, descricao string, dataInicio time.Time) Pais {
	return Pais{Codigo: codigo, Descricao: descricao, DataInicio: dataInicio}
}

func (p Pais) ValueOfCodigo(codigo int) (Pais, error) {
	for _, pais := range paises {
		if pais.Codigo == codigo {
			return pais, nil
		}
	}
	return Pais{}, errors.New("Pais não encontrado")
}

